class Solution
{
    public:
    //Function to return a list of nodes visible from the top view 
    //from left to right in Binary Tree.
    vector<int> topView(Node *root)
    {
        // stores the output
        vector<int> output;
        
        // the helping datastructures
        map<int, int> nodeColMap;
        queue<pair<Node*, int>> trackQ;
        
        // pushing the first nod einto the trackQ
        trackQ.push({root, 0});
        
        while(!trackQ.empty()){
            auto curr = trackQ.front();
            // unpack the pair
            trackQ.pop();
            Node* node = curr.first;
            int col = curr.second;
            // is the column is already visited that means we have fiound 
            // the top most elemnt or the top view element them do not add it to the map
            if(nodeColMap.find(col) == nodeColMap.end()){
                nodeColMap[col] = node->data;
            }
            if(node->left) trackQ.push({node->left, col-1});
            if(node->right) trackQ.push({node->right, col+1});
            
        }
        
        for(auto it: nodeColMap){
            output.push_back(it.second);
        }
        
        return output;
    }

};



